package Model;

/**
 *
 * @author ander
 */
public class Supplier{
    private int id = 0; // nit_proveedor
    private String city = ""; // ciudad_proveedor
    private String address = ""; // direccion_proveedor
    private String name = ""; // nombre_proveedor
    private String phone = "";  // telefono_proveedor

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }
}
